"""``GET /health`` — Java side gates ``isAvailable()`` on this returning 200."""
from __future__ import annotations

from atmosphere_crewai_bridge import __version__


def test_health_returns_ok_with_version(client) -> None:
    response = client.get("/health")
    assert response.status_code == 200
    body = response.json()
    assert body["status"] == "ok"
    assert body["version"] == __version__
